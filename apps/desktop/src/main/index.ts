import { app, BrowserWindow } from 'electron';
import { createMainWindow } from './windows';
import { setupMenu } from './menu';

app.whenReady().then(() => {
  setupMenu();
  createMainWindow();

  app.on('activate', () => {
    if (BrowserWindow.getAllWindows().length === 0) {
      createMainWindow();
    }
  });
});

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});
